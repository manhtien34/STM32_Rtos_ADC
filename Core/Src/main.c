void Task_read(void const * argument){
while(1){
	uint32_t value;
  HAL_ADC_Start(&hadc1);
  osDelay(200);   //  get 1 value with each 200s
	value = HAL_ADC_GetValue(&hadc1);
	osMessagePut(Queue_Handle,value,10);   
	HAL_ADC_Stop(&hadc1);
	osDelay(1000);
 }
}
void Task_show(void const * argument){
	osEvent get_value;
	while(1){
	get_value=osMessageGet(Queue_Handle,osWaitForever);
  response_print2(" \n  Value is :%d \n",get_value.value.v);	
  osDelay(1000);
  }
}


